import pytest


# Параметризованный тест
@pytest.mark.parametrize("a, b, expected", [
    (2, 3, 5),        # положительные числа
    (-5, -3, -8),     # отрицательные числа
    (10, -5, 5),      # положительное + отрицательное
    (0, 0, 0),        # нули
    (0, 15, 15),      # ноль + число
    (2.5, 3.1, 5.6),  # дробные числа
    (999, 1, 1000),   # большие числа
])
def test_addition_parametrized(a, b, expected):
    """Тестируем сложение с разными данными"""
    result = a + b
    assert result == expected, f"{a} + {b} должно быть {expected}, но получилось {result}"
